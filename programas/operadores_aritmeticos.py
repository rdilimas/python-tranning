

produto_1 = 20
produto_2 = 10


print(produto_1 + produto_2)

print(produto_1 - produto_2)

print(produto_1 / produto_2)

#Divisão com resultado inteiro
print(produto_1 // produto_2)

print(produto_1 * produto_2)

#modulo ou resto da divisao
print(produto_1 % produto_2)

#exponenciacao
print(produto_1 ** produto_2)

#respeita a precedencia
x = 10 + 5 * 4
print(x)

#é interessante deixar a ordem explicita
x = (10 + 5) * 4
print(x)