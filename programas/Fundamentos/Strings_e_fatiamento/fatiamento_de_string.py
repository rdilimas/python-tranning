
nome = "Robson Lima da Silva"

#Pega o caracter na posicao do indice enviado
print(nome[0])

print(nome[:-1])

#Da posicao 0 até 9
print(nome[:9])

#da posicao 10 até o final da string
print(nome[10:])

#inicio e fim, da posicao 7 até a 12
print(nome[7:12])

#inicio e fim, da posicao 7 até a 12, pegando um e pulando outro
print(nome[7:12:2])

#pega toda string
print(nome[:])

#pega a string de tras para a frente
print(nome[::-1])




